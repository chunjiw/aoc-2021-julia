"""
Grow the population for one day.
"""
function grow(census::Vector{Int})
    moms = census[1]
    for i in 1:8
        census[i] = census[i+1]
    end
    census[7] += moms
    census[9] = moms
end

function grow(population::Vector{Int}, ndays::Int)
    census = [count(population .== c) for c in 0:8]
    for i in 1:ndays
        grow(census)
    end
    sum(census)
end

